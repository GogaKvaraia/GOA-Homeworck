# Input two numbers
num1 = 10
num2 = 5

# Comparing with different operators
print(f"{num1} > {num2}: {num1 > num2}")   # Greater than
print(f"{num1} < {num2}: {num1 < num2}")   # Less than
print(f"{num1} == {num2}: {num1 == num2}") # Equal to
print(f"{num1} != {num2}: {num1 != num2}") # Not equal to
print(f"{num1} >= {num2}: {num1 >= num2}") # Greater than or equal to
print(f"{num1} <= {num2}: {num1 <= num2}") # Less than or equal to





# Test comparison operators with different values
print("5 > 3:", 5 > 3)
print("10 == 10:", 10 == 10)
print("7 < 2:", 7 < 2)




# Define a variable and reassign its value
boolean_value = True
print("Original Value:", boolean_value)

# Reassign the value to its opposite
boolean_value = not boolean_value
print("Reassigned Value:", boolean_value)




